// Bỏ mọi ký tự không phải số (+81 đầu -> 0).
export function stripPhone(raw: string): string {
  return String(raw).replace(/^\+81/, '0').replace(/\D/g, '');
}

// Chèn hyphen theo đúng logic Phone_Normalization.js:
//   11 số (di động/050…) -> 3-4-4; 10 số -> theo bảng 市外局番 (5/4/3/2 số đầu),
//   không khớp -> 3-3-4. Độ dài khác giữ nguyên số trần (đang gõ dở / số lạ).
export function formatJapanesePhone(raw: string): string {
  const d = stripPhone(raw);
  const len = d.length;

  if (len === 11) {
    return `${d.slice(0, 3)}-${d.slice(3, 7)}-${d.slice(7)}`;
  }

  if (len === 10) {
    const p5 = d.slice(0, 5);
    const p4 = d.slice(0, 4);
    const p3 = d.slice(0, 3);
    const p2 = d.slice(0, 2);

    if (AREA_5.includes(p5)) return `${p5}-${d.slice(5, 6)}-${d.slice(6)}`;
    if (AREA_4.includes(p4)) return `${p4}-${d.slice(4, 6)}-${d.slice(6)}`;
    if (AREA_3.includes(p3)) return `${p3}-${d.slice(3, 6)}-${d.slice(6)}`;
    if (AREA_2.includes(p2)) return `${p2}-${d.slice(2, 6)}-${d.slice(6)}`;

    return `${d.slice(0, 3)}-${d.slice(3, 6)}-${d.slice(6)}`;
  }
  return d;
}
